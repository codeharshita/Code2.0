
public class Constructor1 {
	
	
		
		 int height;
		 int weight;
		 String color;
	
	

	public static void main(String[] args) {
		Constructor1 c = new Constructor1();
		c.color = "Red";
		c.height=5;
		c.weight=76;
		
		System.out.println(c.color);
		System.out.println(c.weight);
		System.out.println(c.height);
			
	}

}
