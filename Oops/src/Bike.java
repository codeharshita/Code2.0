public class Bike {
	
	String name;
	int wheels;
	String model;
	String color;
	boolean isActive;
	
	// To initialize the ready-made value we will nuit constructor.
	
	public Bike(String name, int wheels, String model, String color, boolean isActive) {
		this.name= name;
		this.wheels= wheels;
		this.model= model;
		this.isActive=isActive;
		this.color= color;
	}
	
	


}
