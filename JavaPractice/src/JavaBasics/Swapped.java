// Java program for swapping numbers.
package JavaBasics;


public class Swapped {

	public static void main(String[] args) {
				int a = 2;
				int b = 5;
				
				int temp ;
			System.out.println("The number before swapping is "+ a + b);
				temp = a;
				a = b;
				b = temp;
				
			System.out.println("The number after swapping is "+ a + b );
			
	}

}
