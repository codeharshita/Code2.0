package JavaBasics;

public class Grade {

	public static void main(String[] args) {
		int grade = 100;
		if(grade > 90) {
			System.out.println("You are assigned Grade A.");
			
		}else if(grade > 75) {
			System.out.println("You are assigned Grade B");
		}else if (grade > 65) {
			System.out.println("You are assigned Grade C");
		}

	}

}
